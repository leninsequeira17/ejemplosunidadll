package Ciclos;

public class DemoCicloFor {
	public static void main(String[] args) {
		System.out.println("Primer ciclo for, controlado con contador");
		for(int contador=0; contador<3; contador++) {
			System.out.println("Contador="+contador);
		}
		System.out.println("Fin del primer ciclo for, controlado con contador");
	}
}
